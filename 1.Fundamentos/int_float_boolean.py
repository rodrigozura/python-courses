x = 10 # int
y = 10.523 # float
z = 1.2e5 # float (1.2 * 10^5)
a = 1.2e-5 # float (1.2 * 10^-5)
print(type(x)) # Imprime el tipo de la variable x
print(type(y)) # Imprime el tipo de la variable y
print(z) # Imprime el valor de la variable z
print(a) # Imprime el valor de la variable a

print(x + x) # Suma de dos enteros
print(x + y) # Suma de dos floats
print(y + y) # Suma de dos floats

is_true = True # Booleano verdadero
is_false = False # Booleano falso
print(is_true) # Imprime el valor de is_true
print(is_false) # Imprime el valor de is_false
is_true = not is_false # Invertir el valor de is_false
is_false = not is_true # Invertir el valor de is_true
print(is_true) # Imprime el valor de is_true
print(is_false) # Imprime el valor de is_false